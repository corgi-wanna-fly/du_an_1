    private void showText() {
        new Thread() {
            @Override
            public void run() {
                String slogan[] = {"STOP", "THINKING", "START", "DOING"};
                while (true) {
                    String s = "";
                    for (int i = 0; i < slogan.length; i++) {
                        s += slogan[i] + " ";
                        lblSlogan.setText(s);
                        try {
                            Thread.sleep(1000);
                        } catch (InterruptedException ex) {
                            Logger.getLogger(Display.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }

                }
            }
        }.start();
    }

