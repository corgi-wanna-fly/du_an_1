    private void showCount(){
        //lấy số lượng thành viên
        int countMember = ThanhVienDAO.selectAll().size();
        String tv1 = lblThanhVien.getText();
        String tv2 = String.valueOf(countMember);
        String text1 = String.format("<html>%s<br/>%s</html>", tv1, tv2);
        lblThanhVien.setText(text1);
        //lấy số lượng cấp độ
        int countCapDo = CapDoDAO.selectAll().size();
        String cd1 = lblLevel.getText();
        String cd2 = String.valueOf(countCapDo);
        String text2 = String.format("<html>%s<br/>%s</html>", cd1, cd2);
        lblLevel.setText(text2);
        //lấy số lượng đề thi
        int countDeThi = DeThiDAO.getCount();
        String dt1 = lblDeThi.getText();
        String dt2 = String.valueOf(countDeThi);
        String text3 = String.format("<html>%s<br/>%s</html>", dt1, dt2);
        lblDeThi.setText(text3);
    }
