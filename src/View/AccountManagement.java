    //hàm lấy thông tin từ form
    private String[] getForm() {
        String[] info = new String[4];
        info[0] = txtHoTen.getText();
        info[1] = String.valueOf(txtMatKhau.getPassword());
        info[2] = txtEmail.getText();
        info[3] = hinhAnh;
        return info;
    }

    //update tv
    private boolean insert(int id, String[] s){
        int count = -1;
        String sql = "update ThanhVien set HoTen=?, MatKhau=?, Email=?, HinhAnh=? where MaTV=?";
        try(PreparedStatement ps = DatabaseConnection.getDBConnection().prepareStatement(sql)) {
            for(int i = 1; i <= 4; i++){
                ps.setString(i, s[i-1]);
            }
            ps.setInt(5, id);
            count = ps.executeUpdate();
        } catch (Exception e) {
            e.printStackTrace();
        }
        return count == 1;
    }
