    //hàm hiển thị thông tin lên form
    private void showDetail(ThanhVien tv) {
        txtHoTen.setText(tv.getHoTen());
        txtHoTen.setEnabled(false);
        lblTaiKhoan.setText(tv.getTaiKhoan());
        txtMatKhau.setText(tv.getMatKhau());
        txtMatKhau.setEnabled(false);
        rdoNam.setSelected(tv.getGioiTinh().equals("Nam") || tv.getGioiTinh().equals("Male"));
        rdoNu.setSelected(tv.getGioiTinh().equals("Nữ") || tv.getGioiTinh().equals("Female"));
        txtEmail.setText(tv.getEmail());
        txtEmail.setEnabled(false);
        lblVaiTro.setText(tv.getMaVT() == 1 ? "Quản lý" : "Thành viên");
        lblNgayDangKy.setText(dateToString(tv.getNgayDangKy()));

        //hiển thị ảnh
        try {
            File file = new File("src\\Images\\" + hinhAnh);
            Image img = ImageIO.read(file);
            lblHinhAnh.setText(null);
            lblHinhAnh.setIcon(new ImageIcon(img.getScaledInstance(215, 282, 0)));
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        btnUpload.setEnabled(false);
    }

    //hàm kiểm tra dữ liệu nhập vào
    private boolean validateInfo(){
        JLabel jLabel[] = {lblHoTen, lblMatKhau, lblEmail};
        JTextField jTextField[] = {txtHoTen, txtMatKhau, txtEmail};
        for(int i = 0; i < jLabel.length; i++){
            if(jTextField[i].getText().trim().length() == 0){
                JOptionPane.showMessageDialog(this, "Không để trống " + jLabel[i].getText());
                jTextField[i].requestFocus();
                return false;
            }
        }
        //kiểm tra mật khẩu
        if(String.valueOf(txtMatKhau.getPassword()).trim().length() < 6){
            JOptionPane.showMessageDialog(this, "Mật khẩu không đủ mạnh (>6 kí tự)");
            txtMatKhau.requestFocus();
            return false;
        }
        //kiểm tra email
        if(!txtEmail.getText().trim().endsWith("@fpt.edu.vn")){
            JOptionPane.showMessageDialog(this, "Sử dụng mail fpt");
            txtEmail.requestFocus();
            return false;
        }
        return true;
    }
    //hàm đổi date -> string
    private String dateToString(Date date) {
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        return sdf.format(date);
    }

    //hàm cho phép cập nhật thông tin
