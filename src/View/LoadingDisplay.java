    public void runCar() {
        int flag = (int) lblCar.getX();

        Timer timer = new Timer(16, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent ae) {
                int x = (int) lblCar.getX();
                int y = (int) lblCar.getY();
                if (x == 470) {
                    x = 1000;
                } else {
                    x++;
                }
                lblCar.setLocation(x, y);
            }
        });
        timer.start();
    }
    
